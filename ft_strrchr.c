#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int	i;

	while (str[i] != '\0')
	{
		if (str[i] == (char)c)
			return ((char *)str + i);
		i++;
	}
	if ((char)c == '\0')
		return ((char *)str + i);
	return (NULL);
}
